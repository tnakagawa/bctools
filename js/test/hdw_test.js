function test(name, tv) {
    document.getElementById("result").innerHTML = "";
    log("HDW " + name + " BEGIN");
    let hdw = new HDW();
    let seed = tv.seed;
    let total = 0;
    let ok = 0;
    for (let test of tv.data) {
        var key = hdw.derive(test.chain, seed);
        if (key.xprv == test.extprv && key.xpub == test.extpub) {
            log("OK Chain:" + test.chain);
            ok++;
        } else {
            log("NG Chain:" + test.chain);
            log(test);
            log(key);
        }
        total++;
    }
    if (ok == total) {
        log("ALL OK " + ok + " / " + total);
    } else {
        log("TEST NG " + ok + " / " + total);
    }
    log("HDW " + name + " END");
}

function log(msg) {
    let p = document.getElementById("result");
    let e = document.createElement("div");
    e.innerText = JSON.stringify(new Date(new Date().getTime() + 9 * 60 * 60 * 1000)).substring(12, 24) + " " + msg;
    p.insertBefore(e, p.firstChild);
}

